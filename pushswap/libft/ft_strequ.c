int		ft_strequ(const char *s1, const char *s2)
{
	unsigned int	i;

	if (!s1 || !s2)
		return (0);
	i = 0;
	while (s1[i] == s2[i])
	{
		++i;
		if (!s1[i] && !s2[i])
			return (1);
	}
	return (0);
}
